Necta Colibri on oiva valinta kaikille työpaikoille, missä arvostetaan hyvää papukahvia. Paperillinen suodatinyksikkö takaa korkean kahvin laadun. Juomavalikoima kattavaa juomat tuoresuodatetusta kahvista nykyaikaisiin cappuccinoihin ja espressoihin. Kosketusnäyttöä ohjaa elektroninen etupaneeli. Suodatinkahvia kuppi kerrallaan, jolloin kahvimylly jauhaa pavut ennen suodatusta.

